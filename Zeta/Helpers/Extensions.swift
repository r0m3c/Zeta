import Foundation
import SwiftUI

extension Color {
    struct MyTheme {
        static var maroonColor: Color {
            return Color("ColorMaroon")
        }
        
        static var goldColor: Color {
            return Color("ColorGold")
        }
        
        static var navyBlueColor: Color {
            return Color("ColorNavyBlue")
        }
    }
}
